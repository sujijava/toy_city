package ca.sheridancollege.beans;

public class Toy {

	private int id;
	private String toy_name;
	private double toy_price;
	private int toy_qty;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getToy_name() {
		return toy_name;
	}
	public void setToy_name(String toy_name) {
		this.toy_name = toy_name;
	}
	public double getToy_price() {
		return toy_price;
	}
	public void setToy_price(double toy_price) {
		this.toy_price = toy_price;
	}
	public int getToy_qty() {
		return toy_qty;
	}
	public void setToy_qty(int toy_qty) {
		this.toy_qty = toy_qty;
	}
	
	
	
}
